import { createHash } from "node:crypto";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

const root = process.cwd();
const checkOnly = process.argv.includes("--check");
const sourceFiles = [
  "packages/openapi/openapi.yaml",
  "packages/proto/proto/ubag/v1/jobs.proto",
  "packages/shared-schemas/schemas/job-request.schema.json",
  "packages/shared-schemas/schemas/job-response.schema.json",
  "packages/shared-schemas/schemas/job-event.schema.json",
  "packages/shared-schemas/schemas/error.schema.json"
];

const sources = sourceFiles.map((path) => {
  const text = readFileSync(join(root, path), "utf8");
  return { path, text, sha256: sha256(text) };
});
const openapi = sources.find((item) => item.path.endsWith("openapi.yaml"))?.text ?? "";
const restPaths = [...openapi.matchAll(/^  (\/v1\/[^:]+):/gm)].map((match) => match[1]).sort();
const restOperations = extractRestOperations(openapi);
const manifest = {
  generated_at: "1970-01-01T00:00:00.000Z",
  generator: "tools/generate-sdk-contracts.mjs",
  sources: sources.map(({ path, sha256 }) => ({ path, sha256 })),
  rest_paths: restPaths,
  rest_operations: restOperations
};

const outputs = [
  {
    path: "packages/sdk-typescript/src/generated/contract-manifest.ts",
    text: `// Generated by tools/generate-sdk-contracts.mjs. Do not edit by hand.\nexport const UBAG_CONTRACT_MANIFEST = ${JSON.stringify(manifest, null, 2)} as const;\n`
  },
  {
    path: "packages/sdk-python/src/ubag/generated_contract_manifest.py",
    text: `# Generated by tools/generate-sdk-contracts.mjs. Do not edit by hand.\nUBAG_CONTRACT_MANIFEST = ${JSON.stringify(manifest, null, 2)}\n`
  },
  {
    path: "packages/sdk-go/generated_contract_manifest.go",
    text: `// Generated by tools/generate-sdk-contracts.mjs. Do not edit by hand.\npackage ubag\n\nvar UbagContractManifest = ${goLiteral(manifest)}\n`
  }
];

const stale = [];
for (const output of outputs) {
  const outputPath = join(root, output.path);
  if (checkOnly) {
    const existing = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";
    if (existing !== output.text) {
      stale.push(output.path);
    }
    continue;
  }
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, output.text);
}

if (stale.length > 0) {
  console.error(`Generated SDK contract manifests are stale:\n${stale.map((path) => `- ${path}`).join("\n")}`);
  process.exit(1);
}

if (!checkOnly) {
  console.log(`Generated ${outputs.length} SDK contract manifests.`);
}

function sha256(text) {
  return createHash("sha256").update(text).digest("hex");
}

function extractRestOperations(openapiText) {
  const operations = [];
  let currentPath = "";
  let currentOperation = undefined;
  for (const line of openapiText.split(/\r?\n/)) {
    const pathMatch = line.match(/^  (\/v1\/[^:]+):\s*$/);
    if (pathMatch) {
      currentPath = pathMatch[1];
      currentOperation = undefined;
      continue;
    }
    const methodMatch = line.match(/^    (get|post|put|delete|patch):\s*$/);
    if (methodMatch && currentPath) {
      currentOperation = { method: methodMatch[1].toUpperCase(), path: currentPath };
      operations.push(currentOperation);
      continue;
    }
    const operationMatch = line.match(/^      operationId:\s*(\S+)\s*$/);
    if (operationMatch && currentOperation) {
      currentOperation.operation_id = operationMatch[1];
    }
  }
  return operations.sort((left, right) =>
    `${left.path} ${left.method}`.localeCompare(`${right.path} ${right.method}`)
  );
}

function goLiteral(value) {
  if (Array.isArray(value)) {
    if (value.length === 0) return "[]any{}";
    return `[]any{${value.map(goLiteral).join(", ")}}`;
  }
  if (value !== null && typeof value === "object") {
    const entries = Object.entries(value).map(([key, child]) => `${JSON.stringify(key)}: ${goLiteral(child)}`);
    return `map[string]any{${entries.join(", ")}}`;
  }
  return JSON.stringify(value);
}
